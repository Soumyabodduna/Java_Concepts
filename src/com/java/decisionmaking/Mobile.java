package com.java.decisionmaking;

public class Mobile {
    public static void main(String args[])
    {
        int charge = 10;

        if (charge == 10)
            System.out.println("Battery is  10, Please Charge your Mobile");
        else if (charge == 15)
            System.out.println("Battery is  15, Please turn on your battery saver");
        else
            System.out.println("You can use your mobile");
    }

}
